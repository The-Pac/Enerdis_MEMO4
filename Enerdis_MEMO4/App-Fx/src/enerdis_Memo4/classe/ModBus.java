package enerdis_Memo4.classe;

import javafx.collections.ObservableList;
import jssc.SerialPort;

public class ModBus extends Liaison_Serie {
    private final Liaison_Serie liaisonSerie;

    ////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////

    public ModBus() {
        liaisonSerie = new Liaison_Serie();
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////

    public void connecterMaitre(String port) {
        this.liaisonSerie.configurationPort(port, SerialPort.BAUDRATE_9600);
        Liaison_Serie.setModBus(this);
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////

    public void fermerLiaisonSerie() {
        liaisonSerie.fermer_Port();
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////

    public ObservableList<String> liste_ports() {
        return this.liaisonSerie.listePorts();
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////

    public void ecriture(byte[] bytes) {
        this.liaisonSerie.ecriture(bytes);
    }


    ////////////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////


}
